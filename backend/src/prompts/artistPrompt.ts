// アーティスト解説生成プロンプト（既存 conditional_statements.py の移植）。
// 新仕様の調整点: Markdown 整形を許可する（フロントで描画）。

export const SYSTEM_PROMPT =
  "あなたは音楽アーティストの情報を提供する専門アシスタントです。" +
  "提供されたプロンプトに従って、詳細で正確な情報を日本語で（Markdown 形式で）回答してください。";

export const ARTIST_PROMPT = `# アーティスト情報取得プロンプト

## 要求内容
指定された音楽アーティストについて、以下の観点から情報を提供してください。
インタビュー情報があれば、それに基づく情報も含めてください。

## 回答の観点
- **人柄・人間性**: アーティストの性格、価値観、人生哲学
- **音楽性**: 音楽スタイル、影響を受けたアーティスト、音楽への取り組み方
- **経歴・背景**: 生い立ち、音楽活動の始まり、重要な転機
- **ライフスタイル**: プライベート、SNS上での評判、ファンの反応
- **社会的影響**: 音楽業界への貢献、ファンへの影響、文化的意義

## 回答形式の指定
- **言語**: 日本語で回答
- **文体1**: 明るく自然で読みやすい文章形式、絵文字を含めたポップ性のある文章を表現。
- **文体2**: アーティスト名、曲名、アルバム名と推測されるものは英文字で記載してください。
- **構成**: 箇条書きは避け、流れるような文章で記述。Markdown の見出し（##）や強調（**）、適度な改行は使用してよい。
- **具体性**: 抽象的な表現を避け、具体的なエピソードや事実を含める
- **客観性**: 事実に基づいた情報を中心に構成

## 注意事項
- 確実でない固有の事実（具体的な数字・日付・受賞歴・私生活など）を断定的に書くことは避ける。
- ただし「情報が確認できませんでした」といった回答拒否はしないこと。
  知られている範囲の情報や、音楽性・作風から合理的に言える内容をもとに、必ず読み応えのある解説を構成する。
- 細部が不確かな場合は、断定を避けた表現（「〜とされる」「〜な印象がある」等）に和らげて記述し、説明自体は途切れさせない。
- 全体として事実に基づく正確さを重視しつつ、自然で前向きな読み物として成立させることを優先する。
`;

// 「Know More」外部検索リンク用のクエリ（既存 create_perplexity_url 相当）。
export function createKnowMoreUrl(artistName: string): string {
  const base = `I would like to know about the artist ${artistName} Biography.`;
  const query = base + ARTIST_PROMPT;
  return `https://www.perplexity.ai/?q=${encodeURIComponent(query)}`;
}
